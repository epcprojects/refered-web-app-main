const alphabet = 'abcdefghijklmnopqrstuvwxyz';
const A_LETTER_CODEPOINT = '1f1e6';
const incrementCodepoint = (codePoint: string, incrementBy: number): string => Number(parseInt(codePoint, 16) + incrementBy).toString(16);
